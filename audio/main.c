void
main(void)
{
	unsigned int	val;
	unsigned int	count;
	unsigned char	peak, pcnt, v;
	static bit		on;

	count = 4000;
	pcnt = 0;
	PORT_init();
	CLK_init();

	//EA = 1;
	for(;;) {
		PCA0CPH4 = 0;		// kick the watchdog
		P2_BITS.B5 = 0;
		while(AD0INT == 0)
			continue;
		P2_BITS.B5 = 1;
		val = ADC0L + (ADC0H << 8);
		AD0INT = 0;
		SBUF0 = ulaw[val];
		// flash the power led at a 1 Hz rate
		count--;	
		if(count == 0) {
			count = 4000;
			on = !on;
			GREEN(on);
		}
		// update the VU led every so many samples
		if(pcnt == 0) {
			PCA0CPH0 = peak;
			peak = 0xFF;
			pcnt = 8;
		}
		pcnt--;
		// half wave rectify the signal for the peak VU meter
		if(val & 0x200) {
			v = val >> 1;		// look at top 8 bits for VU only
			v ^= 0xFF;
			if(v < peak)
				peak = v;
		}
	}
}


