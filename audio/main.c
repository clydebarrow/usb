// choose which pins the activity and power leds are on, and
// their polarity

#define	POWER_LED		(P2_BITS.B2)
#define	ACTIVITY_LED	(P2_BITS.B3)
#define	ACTIVE(x)		ACTIVITY_LED = !(x)
#define	GREEN(x)		POWER_LED = !(x)

// crystal frequency is 11.0592MHz
// but for now, use the internal oscillator.

// Timer 2 reload period

#define	SYSCLK	(24000000UL*4/2)		// system clock freq
#define	T2PRE	12				// timer 2 prescaler
#define	T2RATE	128UL				// timer 2 desired interrupt rate - 128Hz

#define	T2LOAD	(SYSCLK/T2PRE/(T2RATE-1))	// reload value for T2

static bit				active;			// busy doing something
static unsigned char	timeout;		// timeout value in ticks

#define	TIMEOUT_VAL(x)	((((x)*T2RATE))/1000+1)
#define	SET_TIMEOUT(x)	(timeout = TIMEOUT_VAL(x))
#define	TIMEDOUT()		(timeout == 0)


static void
CLK_init(void)
{
	unsigned char	i;
	// Clock setup

	CLKMUL = 0x80;			// enable multipler
	i = 40;
	while(--i != 0)
		continue;
	CLKMUL = 0xC0;			// initialize it (whatever that does!)
	while(!(CLKMUL & 0x20))	// wait for multiplier to stabilize
		continue;
	CLKSEL = 2;				// 24MHz SYSCLK, 48MHz USBCLK
	OSCICN = 0x83;			// enable clock

	// Timer 1 is baud rate
	// timer 0 triggers the ADC at 8000 samples/sec

	TH1 = 152;				// baud rate 115200 at 24MHz
	TH0 = 6;				// 8000Hz sample rate
	TMOD = 0B00100010;		// timer 0 and 1 are both mode 2 - 8 bit auto-reload
	CKCON = 0B1000;			// sysclk input to timer 1, sysclk/12 for timer 0
	TR1 = 1;				// enable timer
	TR0 = 1;				// enable timer 0

	SCON0 = 0;				// default UART settings - rx not enabled
	TI0 = 1;				// flag tx buffer empty

	// timer 

	// ADC setup - P2.4 is the single-ended input
	AMX0P = 0B01100;		// P2.4 is +ve input
	AMX0N = 0B11111;		// gnd is -ve input
	//ADC0CF = 0B11000100;	// divide sysclk, left justify
	ADC0CF = 0B11000000;	// divide sysclk, right justify
	ADC0CN = 0B10000001;	// enable adc, convert on timer 0 overflow

	// set up the PWM output and WDT
	PCA0CPL4 = 32;			// will give a WDT interval of 8.4ms
	PCA0CPM0 = 0b01000010;	// 8 bit pwm on channel 0
	PCA0CN = 0b01000000;	// enable the PCA module
	PCA0MD = 0b01100000;	// enable the watchdog
}

static void
PORT_init(void)
{
	// init IO ports

	P0SKIP =  0b11001111;	// skip all except UART
	P1SKIP =  0b11111111;	// skip all
	P2SKIP =  0b11110111;	// skip all except P2.3 - CEX0
	P0MDOUT = 0b00010000;	// Enable TX output
	P0MDIN =  0b01111100;	// all pins analog input except
	P2MDOUT = 0b01101100;	// bits 6,5,3 and 2 are push-pull output
	P2MDIN =  0b01101100;	// all pins analog input except
	XBR0 = 	   0x1;			// enable UART0 pins
	XBR1 =	   0x41;		// enable crossbar and CEX0
	REF0CN = 0B0111;		// enable temp sensor, bias voltage, and Vref output
	GREEN(1);
	ACTIVE(0);
}

